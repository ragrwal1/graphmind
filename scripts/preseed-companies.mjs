import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { parse } from "csv-parse/sync";

const root = process.cwd();
const inputPath = path.join(root, "DB-Snapshot-CSV", "InvestU Captured Opportunities (1).csv");
const supabaseSeedPath = path.join(root, "supabase", "seed-companies.sql");

const normalizeHeader = (value) => value.replace(/^﻿/, "").trim();

const slugify = (value) =>
  value
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-")
    .slice(0, 80);

const stableId = (name, website) => {
  const suffix = createHash("sha1").update(`${name}|${website || ""}`).digest("hex").slice(0, 10);
  return `snapshot-company-${slugify(name)}-${suffix}`;
};

const sqlString = (value) => {
  if (value === null || value === undefined || value === "") return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
};

const sqlArray = (values) => {
  if (!values.length) return "'{}'::text[]";
  return `array[${values.map(sqlString).join(", ")}]::text[]`;
};

// Map Development Stage CSV values to normalized stage
const normalizeStage = (rawStage) => {
  if (!rawStage) return null;
  const s = rawStage.trim().toLowerCase();
  if (s.includes("pre-seed") || s.includes("pre seed") || s.includes("preseed")) return "Pre-Seed";
  if (s.includes("seed")) return "Seed";
  if (s.includes("series a")) return "Series A";
  if (s.includes("series b")) return "Series B";
  if (s.includes("series c")) return "Series C";
  if (s.includes("mvp") || s.includes("prototype")) return "MVP";
  if (s.includes("idea") || s.includes("concept")) return "Idea";
  if (s.includes("revenue") || s.includes("growth")) return "Revenue Stage";
  return rawStage.trim().slice(0, 50);
};

const csv = await readFile(inputPath, "utf8");
const rows = parse(csv, {
  columns: (headers) => headers.map(normalizeHeader),
  skip_empty_lines: true,
  trim: true,
  relax_column_count: true,
});

const companies = rows
  .filter((row) => row.Company?.trim())
  .map((row) => {
    const name = row.Company.trim();
    const website = row.Website?.trim() || null;
    const description = row["Company Description"]?.trim() || null;
    const vertical = row.Vertical?.trim() || null;
    const fiscalYear = row["Fiscal Year"]?.trim() || null;
    const diligenceStatus = row["Diligence Status"]?.trim() || null;
    const stage = normalizeStage(row["Development Stage"]);
    const contactEmail = row["Contact - Email"]?.trim() || null;
    const sourceOrganization = row["Source Organization"]?.trim() || null;

    const aliases = Array.from(
      new Set([
        name,
        ...name.split(/\s+/).filter((part) => part.length > 2),
      ])
    );

    return {
      airtable_id: stableId(name, website),
      name,
      aliases,
      vertical,
      stage,
      diligence_status: diligenceStatus,
      description,
      fiscal_year: fiscalYear,
      website,
      contact_email: contactEmail,
      source_organization: sourceOrganization,
      raw_hash: createHash("sha256").update(JSON.stringify(row)).digest("hex"),
    };
  })
  .sort((a, b) => a.name.localeCompare(b.name));

// Deduplicate by airtable_id (same company submitted multiple years → keep latest row)
const dedupedMap = new Map();
for (const company of companies) {
  dedupedMap.set(company.airtable_id, company);
}
const deduped = Array.from(dedupedMap.values());

const seedSql = `-- Generated by node scripts/preseed-companies.mjs
-- Snapshot source: DB-Snapshot-CSV/InvestU Captured Opportunities (1).csv
-- Total companies: ${deduped.length}

insert into companies (
  airtable_id,
  name,
  aliases,
  vertical,
  stage,
  diligence_status,
  description,
  fiscal_year,
  website,
  contact_email,
  source_organization,
  raw_hash,
  material_change,
  status,
  synced_at
) values
${deduped
  .map(
    (c) => `  (
    ${sqlString(c.airtable_id)},
    ${sqlString(c.name)},
    ${sqlArray(c.aliases)},
    ${sqlString(c.vertical)},
    ${sqlString(c.stage)},
    ${sqlString(c.diligence_status)},
    ${sqlString(c.description)},
    ${sqlString(c.fiscal_year)},
    ${sqlString(c.website)},
    ${sqlString(c.contact_email)},
    ${sqlString(c.source_organization)},
    ${sqlString(c.raw_hash)},
    true,
    'active',
    now()
  )`
  )
  .join(",\n")}
on conflict (airtable_id) do update set
  name = excluded.name,
  aliases = excluded.aliases,
  vertical = excluded.vertical,
  stage = excluded.stage,
  diligence_status = excluded.diligence_status,
  description = excluded.description,
  fiscal_year = excluded.fiscal_year,
  website = excluded.website,
  contact_email = excluded.contact_email,
  source_organization = excluded.source_organization,
  raw_hash = excluded.raw_hash,
  material_change = companies.raw_hash is distinct from excluded.raw_hash,
  status = 'active',
  synced_at = now();
`;

await mkdir(path.dirname(supabaseSeedPath), { recursive: true });
await writeFile(supabaseSeedPath, seedSql);

console.log(`Preseeded ${deduped.length} companies (from ${rows.length} CSV rows)`);
console.log(`Wrote ${path.relative(root, supabaseSeedPath)}`);
